				<h1>What is <b class="orange">SPF?</b> and <b class="orange">PA?</b></h1>
				<p>SPF kepanjangan dari Sun Protection Factor. Ini pengukuran dari berapa kali angka untuk membuat kulit memerah dibawah radiasi UVB dengan sunscreen dibandingkan dengan tidak menggunakan sunscreen. Contohnya, SPF 15 berarti membutuhkan 15 kali untuk kulit dengan sunscreen untuk memerah dibandingkan dengan tidak menggunakan sunscreen. Rule of thumb menjelaskan bahwa, sunscreen SPF 15 melindungi 93% dari sinar UVB; SPF 30 melindungi dari 97%; dan SPF 40, 98%.</p>

				<p>PA adalah pengukuran kemampuan sunscreen dalam melindungi dari radiasi UVA. Ada beberapa PA yang dapat dilihat dipenjelasan sunscreen berupa PA+, PA++, dan PA+++. Untuk PA+ mempunyai perlindungan normal terhadap UVA, PA ++ mempunyai perlindungan kuat terhadap UVA, dan PA+++ mempunyai perlindungan sangat kuat terhadap UVA.</p>

				<p>Profesional pada bidang kesehatan setuju bahwa paling tidak dibutuhkan SPF 15 untuk melindungi dari sinar matahari. Tetapi, SPF yang tinggi tidak berarti sunscreen dapat melindungi dari sinar UVA. Dan bukan berarti sunscreen dengan angka SPF yang tinggi, dapat menjaga dari sinar matahari seharian.</p>
